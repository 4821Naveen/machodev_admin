"use client";

import React, { useEffect, ReactNode } from "react";
import Lenis from "lenis";

export const LenisProvider = ({ children }: { children: ReactNode }) => {
    useEffect(() => {
        const lenis = new Lenis();

        function raf(time: number) {
            lenis.raf(time);
            requestAnimationFrame(raf);
        }

        requestAnimationFrame(raf);

        return () => {
            lenis.destroy();
        };
    }, []);

    return <>{children}</>;
};
